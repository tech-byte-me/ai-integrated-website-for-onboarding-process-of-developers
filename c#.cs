using System;

class Program
{
    static void Main()
    {
        // Print a message to the console
        Console.WriteLine("Hello, World!");

        // Declare variables
        int num1 = 10;
        int num2 = 20;
        int sum = num1 + num2;

        // Print the result of the addition
        Console.WriteLine("The sum of " + num1 + " and " + num2 + " is: " + sum);

        // Wait for user input before closing
        Console.ReadLine();
    }
}
